tileSize = canvas.width / map.length;
function loop(){

	ctx.clearRect(0, 0, w, h);

	for(row=0; row<map.length; row++){
		for(col=0; col<map[row].length; col++){
			
				switch(map[row][col]){

			case 0: 
				//empty

				break;

				case 1: 
				//grass
				ctx.fillStyle = "black";
				ctx.fillRect(col*tileSize, row*tileSize, tileSize, 	tileSize);
				break;
			}
		}
	}

	requestAnimationFrame(loop)
}
